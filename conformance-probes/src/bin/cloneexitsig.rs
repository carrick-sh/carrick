//! clone(2) exit_signal: the low byte of clone's flags is the signal the child
//! delivers to its PARENT on exit (SIGCHLD if that byte == SIGCHLD; a different
//! signum, e.g. SIGUSR1, if the guest asks for it; 0 = no exit signal at all).
//! carrick hardcodes SIGCHLD on every fork-style child exit
//! (carrick-hvf/src/vcpu_kick.rs:321-324 publishes LINUX_SIGCHLD regardless of
//! what the guest requested), so a clone(SIGUSR1) child exit wrongly delivers
//! SIGCHLD instead of SIGUSR1.
//!
//! This probe pins it deterministically WITHOUT relying on async handler-race
//! timing: it BLOCKS both SIGCHLD and SIGUSR1 before the clone, so the
//! exit-generated signal becomes *pending* (not lost, not racing a handler),
//! then uses sigtimedwait() over {SIGCHLD, SIGUSR1} to observe exactly which
//! signum the kernel queued. On Linux the answer is SIGUSR1 (verified on Docker
//! linux/arm64: sigtimedwait returns 10); on carrick today it is SIGCHLD.
//!
//! Determinism / safety:
//!   * Output is booleans only (which signum was dequeued), never pids/times.
//!   * We clone ONLY if the pre-block succeeded. SIGUSR1's default action is
//!     terminate; if the block failed and we cloned anyway, the exit-generated
//!     SIGUSR1 could kill the parent before it reports. Gating the clone on
//!     block_ok removes that hazard and keeps output deterministic.
//!   * The child path is a raw clone with a NULL child stack and no CLONE_VM, so
//!     it shares the parent stack COW; the child therefore ALWAYS _exit()s
//!     immediately (it must never return into main and re-run the probe).
//!   * Every wait is bounded: sigtimedwait has a 5s timeout; waitpid reaps the
//!     (already-exited) child, which is immediate. If the runtime never delivers
//!     anything, sigtimedwait returns -1/EAGAIN and we report false rather than
//!     hanging.
//!   * We reap AFTER dequeuing the signal. The exit signal is generated when the
//!     child becomes a zombie (before reap), so blocking it keeps it pending
//!     across the reap; sigtimedwait consumes it deterministically.
//!
//! Docker seccomp note: legacy clone (SYS_clone) with no CLONE_NEW* namespace
//! bits and exit_signal=SIGUSR1 is permitted by Docker's default seccomp
//! profile (the profile masks only the namespace bits of clone's flags, not the
//! low-byte exit_signal), so the Docker oracle runs this probe normally — unlike
//! clone3 (sysno 435), which Docker filters to ENOSYS.

use conformance_probes::{errno, report};
use core::mem::MaybeUninit;

// Linux aarch64 signums (musl's libc::SIG* match Linux here, but pin literals
// so the low-byte-of-flags packing is unambiguous).
const LINUX_SIGCHLD: i32 = 17;
const LINUX_SIGUSR1: i32 = 10;

/// Raw clone via the aarch64 syscall: clone(flags, child_stack=NULL, ...). A
/// NULL stack with no CLONE_VM gives the child a COW copy of the parent stack
/// (fork-style). The low 8 bits of `flags` are the exit_signal.
unsafe fn raw_clone(flags: u64) -> i64 {
    libc::syscall(libc::SYS_clone, flags as i64, 0i64, 0i64, 0i64, 0i64)
}

fn main() {
    unsafe {
        // Block both candidate exit signals so the child's exit-generated
        // signal is queued pending instead of running a default/handler action.
        let mut set: libc::sigset_t = MaybeUninit::zeroed().assume_init();
        libc::sigemptyset(&mut set);
        libc::sigaddset(&mut set, LINUX_SIGCHLD);
        libc::sigaddset(&mut set, LINUX_SIGUSR1);
        let block_ok =
            libc::sigprocmask(libc::SIG_BLOCK, &set, core::ptr::null_mut()) == 0;

        let mut clone_rc_positive = false;
        let mut got_usr1 = false;
        let mut got_chld = false;

        // Only clone once the exit signal is safely blocked. If the block
        // failed, an unblocked SIGUSR1 (default: terminate) generated by the
        // child exit could kill us before we report — so skip and report false.
        if block_ok {
            // clone with SIGUSR1 as the exit_signal (low byte of flags). No
            // other clone flags: a plain fork-style child that reports SIGUSR1
            // on exit.
            let r = raw_clone(LINUX_SIGUSR1 as u64);
            if r == 0 {
                // Child: COW stack shared with parent — exit immediately, never
                // fall through into the rest of main.
                libc::_exit(0);
            }
            clone_rc_positive = r > 0;

            if clone_rc_positive {
                // Dequeue the pending exit signal deterministically.
                // sigtimedwait returns the signum that was delivered; bound it
                // at 5s so a runtime that delivers nothing reports false instead
                // of hanging.
                let mut info: libc::siginfo_t = MaybeUninit::zeroed().assume_init();
                let ts = libc::timespec { tv_sec: 5, tv_nsec: 0 };
                let sig = libc::sigtimedwait(&set, &mut info, &ts);
                // EINTR (interrupted by some other signal) — retry once within
                // the bound.
                let sig = if sig == -1 && errno() == libc::EINTR {
                    libc::sigtimedwait(&set, &mut info, &ts)
                } else {
                    sig
                };
                got_usr1 = sig == LINUX_SIGUSR1;
                got_chld = sig == LINUX_SIGCHLD;
                // Reap the (already-exited) child so we leak no zombie. Bounded:
                // the child is gone, waitpid returns immediately.
                let mut status = 0i32;
                let _ = libc::waitpid(r as i32, &mut status, 0);
            }
        }

        report!(
            block_ok = block_ok,
            clone_rc_positive = clone_rc_positive,
            // Linux: true (exit_signal honored). carrick today: false.
            exit_signal_is_usr1 = got_usr1,
            // Linux: false. carrick today: true (hardcoded SIGCHLD).
            exit_signal_is_chld = got_chld,
        );
    }
}